Angle = float(input("What type of Angle is ... degrees?" ))

if  Angle < 90:
    print("Acute Angle")
elif Angle == 90:
    print("Right-Angle")
elif Angle < 180 :
    print("Obtuse-Angle")
elif Angle == 180:
    print("Straight Line")
elif Angle < 360 :
    print("Reflex-Angle")
elif Angle == 360:
    print("Circle")
